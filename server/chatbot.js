function searchFAQ(message) {
  const lower = message.toLowerCase().trim();
  let bestMatch = null;
  let bestScore = 0;

  for (const entry of FAQ) {
    let score = 0;
    for (const keyword of entry.keywords) {
      if (lower.includes(keyword)) {
        score += keyword.length;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      bestMatch = entry;
    }
  }

  return bestMatch;
}

export function chatbotRoutes(app) {
  app.post('/api/chatbot', async (req, res) => {
    try {
      const { message } = req.body;
      if (!message || !message.trim()) return res.status(400).json({ error: 'message required' });

      const match = searchFAQ(message);

      if (match) {
        return res.json({
          answer: match.answer,
          category: match.category,
          confidence: 'high',
          suggestions: FAQ.filter(e => e.category !== match.category).slice(0, 3).map(e => e.keywords[0])
        });
      }

      const stats = {
        total_products: (await getAll('SELECT COUNT(*)::int as c FROM products WHERE is_active = true'))[0]?.c || 0,
        total_sellers: (await getAll("SELECT COUNT(*)::int as c FROM users WHERE role = 'seller'"))[0]?.c || 0
      };

      res.json({
        answer: 'Извините, я не нашёл ответ на ваш вопрос. Попробуйте переформулировать или напишите в поддержку. Вот что я могу: помочь с покупкой, доставкой, возвратом, оплатой и настройками аккаунта.',
        category: 'general',
        confidence: 'low',
        suggestions: ['Как купить товар?', 'Как открыть спор?', 'Как пополнить баланс?'],
        stats
      });
    } catch (e) {
      res.status(500).json({ error: e.message });
    }
  });

  app.get('/api/chatbot/faq', async (req, res) => {
    try {
      const { category } = req.query;
      let items = FAQ;
      if (category) items = FAQ.filter(e => e.category === category);
      res.json(items.map(({ keywords, answer, category }) => ({ question: keywords[0], keywords, answer, category })));
    } catch (e) {
      res.status(500).json({ error: e.message });
    }
  });
}

export default { chatbotRoutes };
